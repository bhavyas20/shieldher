'use client';

import { useEffect, useState } from 'react';
import { createClient } from '@/lib/supabase/client';
import { type Report } from '@/lib/types';
import RiskBadge from '@/components/RiskBadge';
import LoadingSpinner from '@/components/LoadingSpinner';
import {
  FileDown,
  Download,
  Trash2,
  Clock,
  FileText,
  AlertTriangle,
  Shield,
  ArrowLeft,
} from 'lucide-react';
import Link from 'next/link';
import styles from './page.module.css';

export default function DownloadsPage() {
  const [reports, setReports] = useState<Report[]>([]);
  const [loading, setLoading] = useState(true);
  const [deletingId, setDeletingId] = useState<string | null>(null);
  const [confirmDeleteId, setConfirmDeleteId] = useState<string | null>(null);

  useEffect(() => {
    fetchReports();
  }, []);

  async function fetchReports() {
    const supabase = createClient();
    const { data: { user } } = await supabase.auth.getUser();
    if (!user) return;

    const { data } = await supabase
      .from('reports')
      .select('*')
      .eq('user_id', user.id)
      .order('created_at', { ascending: false });

    if (data) setReports(data);
    setLoading(false);
  }

  const handleDownload = (report: Report) => {
    const link = document.createElement('a');
    link.href = report.file_url;
    link.download = report.file_name;
    link.target = '_blank';
    document.body.appendChild(link);
    link.click();
    document.body.removeChild(link);
  };

  const handleDelete = async (reportId: string) => {
    if (confirmDeleteId !== reportId) {
      setConfirmDeleteId(reportId);
      return;
    }

    setDeletingId(reportId);
    setConfirmDeleteId(null);

    try {
      const res = await fetch('/api/delete-report', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ reportId }),
      });

      if (!res.ok) {
        throw new Error('Failed to delete report');
      }

      setReports((prev) => prev.filter((r) => r.id !== reportId));
    } catch (err) {
      console.error('Delete error:', err);
      alert('Failed to delete report. Please try again.');
    } finally {
      setDeletingId(null);
    }
  };

  if (loading) {
    return (
      <div className={styles.page}>
        <div className={styles.loadingWrap}>
          <LoadingSpinner text="Loading reports..." />
        </div>
      </div>
    );
  }

  return (
    <div className={styles.page}>
      <Link href="/dashboard" className={styles.back}>
        <ArrowLeft size={16} />
        Back to Dashboard
      </Link>

      <div className={styles.header}>
        <h1 className={styles.title}>Downloads</h1>
        <p className={styles.subtitle}>
          Your generated evidence reports — securely download or manage them here.
        </p>
      </div>

      {/* Info banner with glass effect */}
      <div className={styles.infoBanner}>
        <Shield size={24} className={styles.infoBannerIcon} />
        <div>
          <h3 className={styles.infoBannerTitle}>Certified Evidence</h3>
          <p className={styles.infoBannerText}>
            These PDF reports are digitally certified documents generated by ShieldHer&apos;s AI. 
            They can be shared with a <strong>lawyer, counselor, or law enforcement</strong> as supporting material.
          </p>
        </div>
      </div>

      {reports.length > 0 ? (
        <div className={styles.list}>
          {reports.map((report) => (
            <div key={report.id} className={styles.reportCard}>
              <div className={styles.reportMain}>
                <div className={styles.reportIcon}>
                  <FileText size={28} />
                </div>
                <div className={styles.reportInfo}>
                  <span className={styles.reportName}>{report.file_name}</span>
                  <div className={styles.reportMetaRow}>
                    <div className={styles.reportMeta}>
                      <Clock size={14} />
                      <span>
                        {new Date(report.created_at).toLocaleDateString('en-US', {
                          month: 'short',
                          day: 'numeric',
                          year: 'numeric',
                          hour: '2-digit',
                          minute: '2-digit',
                        })}
                      </span>
                    </div>
                    <RiskBadge level={report.risk_level} size="sm" />
                  </div>
                </div>
              </div>

              <div className={styles.reportActions}>
                <button
                  className={styles.downloadBtn}
                  onClick={() => handleDownload(report)}
                  title="Download report"
                >
                  <Download size={16} />
                  <span>Download</span>
                </button>
                <button
                  className={`${styles.deleteBtn} ${confirmDeleteId === report.id ? styles.deleteBtnConfirm : ''}`}
                  onClick={() => handleDelete(report.id)}
                  disabled={deletingId === report.id}
                  title={confirmDeleteId === report.id ? 'Click again to confirm' : 'Delete report'}
                >
                  {deletingId === report.id ? (
                    <span>Deleting...</span>
                  ) : confirmDeleteId === report.id ? (
                    <>
                      <AlertTriangle size={16} />
                      <span>Confirm Delete</span>
                    </>
                  ) : (
                    <>
                      <Trash2 size={16} />
                      <span>Delete</span>
                    </>
                  )}
                </button>
              </div>
            </div>
          ))}
        </div>
      ) : (
        <div className={styles.empty}>
          <div className={styles.emptyIconWrap}>
            <FileDown size={40} />
          </div>
          <h3>No reports generated yet</h3>
          <p>
            Go to any analysis result and click &quot;Download PDF Report&quot; to generate
            your first piece of certified evidence.
          </p>
          <Link href="/dashboard/history" className={styles.emptyButton}>
            Browse History
          </Link>
        </div>
      )}
    </div>
  );
}
